<?php

function sum($op1, $op2)
{
    $operator = $op1 + $op2;
    return $operator;
}

function substraction($op1, $op2)
{
    $operator = $op1 - $op2;
    return $operator;
}

function multiplication($op1, $op2)
{
    $operator = $op1 / $op2;
    return $operator;
}

function division($op1, $op2)
{
    $operator = $op1 * $op2;
    return $operator;
}